	<section>
		<h2>
			Phase 2: Immerse yourself in the problem
		</h2>
		<p>
			Visioning allows us to both understand product usage, and the larger task of getting to know a new domain or
			industry. To explore a broad area like learning which encompasses everything from math to makeup tutorials
			we employed a 360-degree strategy for immersion, seeking insights from a variety of perspectives:
		</p>
		<ul>
			<li><b>Industry basics:</b> We audited our existing product (YouTube) and evaluated the current options
				available in the industry to all kinds of learners. This gave us a solid foundation for the current
				state of our product and the industry at large.</li>
			<li><b>Experts and thought leaders:</b> We met with members of the World Economic Forum to help us
				understand job reskilling trends; we were inspired by programs like Arizona State University and their
				mission to democratize education by making it accessible to everyone. Insights from these experts gave
				us a glimpse into the future, and provided great inspiration for new ideas.
			</li>
			<li>
				<b>Empathy exercises:</b> Team members enrolled in online courses to directly experience the learning
				process. These deeply personal experiences revealed insights, particularly on the challenges of staying
				motivated to learn.
			</li>
			<li> <b>The learning ecosystem:</b> In addition to understanding our audience of people using YouTube to
				learn something new, we broadened our research plan to include creators of educational content on
				YouTube, and employers too. We made sure to invest in research across all three user types to ensure
				that we designed solutions that created value for the entire ecosystem.
			</li>
			<li><b>The whole person:</b>
				While data can be useful in understanding user behavior, understanding people on a deeper level requires
				getting out of the office and into the field. For this project, we traveled to different parts of the
				country to meet all kinds of learners and to better understand the person behind each individual user.
				We spent time with a donut glazer in Salt Lake City who was seeking a better career path but didn't see
				any viable possibilities. He revealed that he grew up fatherless and lacked a professional role model to
				guide him. From stories like these, we learned that for many people the first step of their journey to
				learn something isn't choosing a course, but the more fundamental step of discovering new directions for
				one's life. </li>
		</ul>
