			Throughout my career and most recently at YouTube, I've learned that there's no single definition of
			innovation. Instead, there are many different yet equally valid ways to introduce new ideas, regardless of
			your specific process or mindset. What we've discovered at YouTube over many years and many projects is that
			our teams, products, and ultimately <a href="https://">our users</a>, greatly benefit when we use multiple,
			complementary forms of innovation.
		</p>

		<p>
			Our product teams focus on two approaches: versioning and visioning. Versioning uses incremental product
			improvements that add up over time to significantly improve user experience. Visioning, in contrast, is an
			approach that addresses more open-ended questions and emphasizes longer-term thinking. At YouTube, we use
			visioning to explore uncharted terrain in the form of new users and/or new problem spaces. While perhaps
			lesser known, visioning is a technique you too can use to speed up product innovation.
		</p>
